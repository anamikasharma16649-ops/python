#  lists method


l1 = [1,8,7,2,21,15]
print(l1)

# sort method

l1.sort()

print(l1)

# reverse method

l1.reverse()
print(l1)

#  add = append

l1.append(100)
print(l1)

# insert method

l1.insert(2,"radha")
print(l1)

# pop = delete

l1.pop(3)
print(l1)

# remove

l1.remove(100)
print(l1)




